// Look! I have already done my home task! - Смотри! Я уже сделал свое домашнее задание!


// Present Perfect. В каких случаях использовать
// Итак, форма I have done, она же Present Perfect — настоящее совершенное время. В названии часть Perfect — совершенное, говорит о действии, которое уже произошло, совершилось в прошлом, а вот слово Present не дает ему стать историей, а привязывает к настоящему.
// Простой пример: I have cleaned my flat. – Я убрала квартиру. Убрала — да, прошлое, но в квартире сейчас так хорошо и чисто, что никак не хочется, чтобы моя уборка осталась в прошлом. И нам, по большому счету, не нужно время, когда именно это было сделано, процесс, как долго это происходило. Наша цель — результат, который мы получаем и видим сейчас.
// I have done – Present Perfect – настоящее совершенное время
// Present – в настоящем видим результат того, что Perfect – уже произошло
// Have done, have cleaned – сделала, убрала – есть результат трудов
// I have cleaned my flat. Don't touch anything. – Я убрала квартиру. Ни к чему не прикасайтесь.
// They have just got married. They are really happy. – Они только что поженились. Они действительно счастливы.


// I have done. Form
// Действия Perfect составные, т.е. состоят из нескольких частей. Итак, чтобы рассказать о том, к чему вы приложили руку и в результате, что такого классного вы имеете сейчас: берем have или has и добавляем действие, которое на этот раз должно быть в так называемой третьей форме. А это что еще такое спросите вы? Рассказываю.
// Когда мы работали с прошедшим простым временем у нас глагол был с окончанием -ed или из второй колонки таблицы неправильных глаголов — это была вторая форма глагола.
// А вот третья форма, которая нужна для настоящего совершенного — это тоже -ed (если глагол правильный) или форма из третьей колонки таблицы неправильных глаголов: break – broke – broken, leave – left – left, go – went – gone, но clean – cleaned – cleaned, cook – cooked – cooked. I have cleaned my flat. He has broken his leg. Или He hasn’t broken his leg. Has he broken his leg?
// Отрицания и вопросы образуются предельно просто: not или меняем порядок слов.
// Present Perfect: Have / has + V3, V3 (cleaned) – правильный / V3 (done) – неправильный
// Утверждаем: I, we, you, they + have + V3 / he, she, it + has + V3
// Отрицаем: I, we, you, they + haven’t + V3 / he, she, it + hasn’t + V3
// Вопрос: Have + I, we, you, they + V3? / Has + he, she, it + V3?
// He has broken his leg. This is really sad. – Он сломал ногу. Это действительно печально.
// I haven't done my homework. Sorry. – Я не выполнил домашнее задание. Простите.
// Have you done your homework? Can I check it? – Вы выполнили домашнее задание? Я могу проверить?


// I have done. Слова-маркеры
// Когда мы работаем с формой have done, запомните, что точного указания, когда именно действие совершилось, не будет. Но, тем не менее, можно назвать несколько слов-маркеров, по которым вы сможете определить, что нужно употребить именно have done. Итак, just – только что.
// Обычно в предложении ставится между have / has и основным глаголом. Работает во всех типах предложений: утверждение, вопрос, отрицание. We have just cleaned our flat.
// Дальше already / yet – уже / еще не. Already – только в утверждениях. В вопросах и отрицании already меняется на yet и ставится в конце предложения. He has already left. He hasn’t left yet. Has he left yet? 
// Есть еще lately / recently – недавно, several times – несколько раз, которые можно употребить в любом типе предложения. He has left recently. – Он недавно ушел. He has tried several times. – Он пытался несколько раз.
// Have done – произошло: ни как, ни когда – важен результат
// Have (haven’t) just done – действие только что произошло
// Have done already – действие уже произошло
// Haven’t done yet – действие еще не произошло / Have you done yet? – действие уже произошло?
// Have done recently / lately / several times – действие произошло недавно / несколько раз
// He has left. And I'm really upset. – Он ушел. И я очень расстроена.
// We have just cleaned our flat. – Мы только что убрали квартиру.
// He hasn't left yet. – Он еще не ушел.
// We have cleaned the window 3 times, but it's still dirty. – Мы помыли окно 3 раза, а оно все еще грязное.









