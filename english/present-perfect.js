

// Действие Perfect

// Я сделала вчера домашнее задание и я делала вчера домашнее задание. Сделала и делала — это 
//два разных действия в английском языке. I did – действие простое – Simple и I was doing – действие 
//длительное – Continuous, хотя оба прошедшие – Past. 
//Точно, как и I do and I am doing – Present Simple и Continuous. Но помимо Simple и Continuous, 
//действие в английском языке может быть еще и Perfect – I have done – уже сделала. Вот 
//сделала и все. На лицо результат — имею выполненное задание, I have done the task – Present Perfect.

// Past: Simple and Continuous – I did and I was doing
// Present: Simple and Continuous – I do and I am doing
// Do – действие: Simple, Continuous + Perfect (уже сделано: ни как, ни когда – результат)
// I have done – Present Perfect – уже сделала


// Yesterday from 5 to 6 I was doing my English home task and I did it quite well. – 
//Вчера с 5 до 6 я делал домашнее задание по английскому и сделал его достаточно хорошо.
// And right now I am doing my home task too. I do it twice a week. – И вот как раз сейчас я 
