

// Past Continuous: I was doing
// Вспомнили что-то из прошлого — рассказали. Вспомнили — рассказали. I did, he did – Сухие факты. Иногда улучшили и добавили I used to do, he used to do – говоря о привычках. Но в любом случае это все самые обычные, простые действия, которые произошли в прошлом.
// В общем Past Simple — прошедшее простое время. Но прошедшее время может быть и не простым. Например, действие не просто произошло, а происходило и происходило, тянулось, длилось, другими словами, было Past Continuous — прошедшее длительное и форма у него соответствующая – I was doing, he was doing, they were doing.

// I did, he did – факты в прошлом / I used to do, he used to do – привычки в прошлом
// I did, he did, I used to do, he used to do – простые действия (Past Simple)
// I was doing, he was doing, they were doing – длительные действия (Past Continuous)

// He did his best on camera yesterday. – Он старался изо всех сил на камеру вчера.
// He used to do his best every time he was on camera. – Он делал все возможное каждый раз, когда оказывался перед камерой.
// He was doing his best while he was playing his role yesterday. – Он старался из всех сил, когда играл свою роль вчера.



// I was doing. Когда и как употреблять
// Итак, говоря I was doing, мы прежде всего подразумеваем некий процесс, который мы застали, так сказать, врасплох. Yesterday at the same time I was watching a horror film. Ага, выбрали же момент. Я же не всегда на работе.
// Итак, I was watching — действие происходило, а не просто произошло. Или, например, Last Saturday from 5 to 6 my cameraman was filming a wedding ceremony. И не смог мой урок снять — снимал свадебную церемонию.
// Видите, мы как бы выхватываем момент из целого события? Yesterday at the same time I was watching / My cameraman was filming. Ни начала, ни конца действия нет.

// Was doing – Continuous – процесс: was watching, was filming
// Did – произошло / was doing – происходило
// Yesterday at the same time, last Saturday from 5 to 6 – момент из целого события

// Yesterday at the same time I was watching a horror film. – Вчера в это же самое время я смотрела фильм ужасов.
// Last Saturday from 5 to 6 my cameraman was filming a wedding ceremony. – В прошлую субботу с 5 до 6 мой оператор снимал свадебную церемонию.



// I was doing. Формы
// I was watching a horror film at the same moment yesterday. Ну это вы уже знаете. А вы вот не смотрели фильм, например. You were not watching. А они? Were they watching? Что ж, в вопросах и отрицаниях ничего сложного нет.
// Нужно помнить, во-первых, что I, he, she, it работает с was, а we, you, they — с were. В отрицаниях добавляем после was / were частицу not. You were not watching. В вопросах изменение порядка слов все решает. Were they watching?

// Отрицание: Кто + was / were not + doing
// Вопрос: Was / were + Кто + doing
// Кто - I, he, she, it + was / Кто – we, you, they + were

// I was dancing all evening yesterday. – Я танцевала весь вечер вчера.
// They were not praising this actor at that moment. – В тот момент они не хвалили этого актера.
// Were you filming that scene from 1 to 6 p.m. last Saturday? - Yes, we were. – Вы снимали эту сцену с часу до шести вечера в прошлую субботу? – Да, снимали.



// I did and I was doing
// Вот смотрела я внимательно и напряженно фильм ужасов вчера. И тут на тебе стук в дверь. Yesterday while I was watching a horror film, somebody knocked at the door. А с вами бывало такое?
// Что-то происходило себе спокойно, и на тебе внезапный стук, звонок, который все поломал. Так вот, в английском языке то, что происходило, будет I was doing. А то, что его поломало I did. My cameraman was filming a video and his phone rang. Was filming – происходило, rang – поломало. My friend was walking last weekend when it suddenly rained. Но бывает и такое, что никто ничего не ломает. While I was watching a film my cat was sleeping nearby. Идиллия прямо.
// While и when в подобных случаях связывающие слова.

// I was doing + He did = происходило + поломало (Past Continuous + Past Simple)
// I was doing + He was doing = происходило + происходило ( Past Continuous + Past Continuous)
// While - в то время как (с “происходило”) / When - когда (с “поломало”)

// My friend was walking last weekend when it suddenly rained. – Моя подруга гуляла на прошлых выходных, когда внезапно пошел дождь.
// While I was watching a film my cat was sleeping nearby. – Пока я смотрела фильм, мой кот спал рядом.
// When my cameraman was filming a video, his phone rang. – Когда мой оператор снимал видео, зазвонил его телефон.













