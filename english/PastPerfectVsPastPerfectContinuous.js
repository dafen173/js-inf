

// Past Perfect. Употребление
// Past Perfect (Simple) – Прошедшее Завершенное или Совершенное, как его часто называют, употребляется, чтобы подчеркнуть наличие определенных результатов на момент в прошлом. Оно показывает действие, произошедшее перед другим действием в прошлом или на момент в прошлом.
// Например: She had already finished the report when her boss arrived. Она уже закончила отчет, когда приехал ее босс. Также оно показывает  действие, завершившееся в прошлом так, что результат от этого действия был виден в прошлом.
// Например: I got the bonus. I had sold 100 mobile phones. Я получила бонус. Я продала 100 мобильных телефонов.

// Past Perfect (Simple) – действие, произошедшее перед другим действием в прошлом
// Past Perfect (Simple) – действие, завершившееся в прошлом

// When you arrived, I had already finished my meal. – Когда ты приехал, я уже закончил свою трапезу.
// They had cleaned their room by 5 o'clock. – Они убрали свою комнату к 5 часам.



// Past Perfect Continuous. Употребление
// Past Perfect Continuous (Прошедшее Совершенное Длительное) – употребляется, чтобы подчеркнуть продолжительность определенного действия на момент в прошлом.
// Если мы хотим сделать акцент на длительности действия, произошедшего в прошлом перед другим действием в прошлом или на момент в прошлом.
// Например: They had been developing the product for 2 months before they launched it. Они разрабатывали продукт 2 месяца, прежде чем запустили его. Действие, продолжавшееся какое-то время в прошлом так, что результат от этого действия был виден в прошлом.
// Например: We were so disappointed. We had been waiting for this event for 3 years.Мы были так разочарованны. Мы ждали этого события 3 года.

// Past Perfect Continuous – акцент на длительности действия в прошлом перед другим действием в прошлом
// Past Perfect Continuous – Действие, продолжавшееся какое-то время в прошлом

// I had been typing this text for 2 hours and then found it on the Internet. – Я набирал этот текст два часа, а потом нашел его в Интернете.



// Had done vs Had been doing
// Итак в чем же разница между этими формами: had done и had been doing? Форма had done относится к Past Perfect – а значит мы делаем акцент на результате.  Свершилось! Сколько делал – не важно.
// Например: I had translated 3 letters when he arrived. Я перевел 3 письма, на момент, когда он приехал. Форма had been doing относится к Past Perfect Continuous – акцент на длительности действия. Говорящий делает акцент на затраченном времени, а количество сделанной работы не упоминает.
// Например: I had been translating for 2 hours when he arrived.  Я переводил уже 2 часа, когда он приехал.

// Past Perfect (had done) – акцент на своем результате, не упоминая затраченного времени 
// Past Perfect Continuous (had been doing) – акцент на затраченном времени

// He was happy. He had talked to Marry. – Он был счастлив. Он поговорил с Мерри.
// He was happy. He had been talking to Marry for 2 hours. – Он был счастлив. Он проговорил с Мерри 2 часа.



// Had done and Had been doing. Особенности
// В Past Perfect Continuous не все глаголы могут употребляться и стать had been doing формой. К таким относятся так называемые state verbs, глаголы в которых нет действия. Глаголы be (быть), know (знать), see (видеть), understand (понимать) и некоторые другие в форме Continuous почти никогда не употребляются, так как отражают скорее состояние, а не действие. Даже если она кажется подходящей, ставится более простое время. В нашем случае — Past Perfect.
// Например:My daughter hadn’t seen a real elephant for a long time until I took her to the zoo . Моя дочь не видела настоящего слона пока я не отвел ее в зоопарк. Также сюда  относятся глаголы, обозначающие действия недлительные, краткие, быстро происходящие: cut, start, begin, finish, stop, find, lose.
// Глаголы hope ( надеяться), intend (намереваться), want ( хотеть), plan (планировать) употребляются в  Past Perfect, чтобы подчеркнуть, что кто-то желал что-то сделать, но не сделал. Had в таких случаях выделяется интонацией: Например: I had hoped to meet him on Sunday. – Я надеялся, что встречусь с ним в воскресенье (но не встретился).

// Past Perfect – состояния: understand, know, hate, consider
// Past Perfect Continuous – только действия: swimming, running, reading
// Past Perfect – hope, intend, want, plan – хотел сделать, но не сделал

