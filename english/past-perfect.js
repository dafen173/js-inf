
// Had done. Past Perfect
// Еще помните три времени, которым подчиняются события нашей жизни? Present, Past и Future. 
//А какая из характеристик действия в английском поможет похвастаться миру своими результатами? 
//Правильно —  cовершенность. Perfect tense.
// И сегодня мы остановимся на уже достигнутом, что мы можем описать формой had done, она же Past Perfect, которая говорит, что действие-то уже свершилось к моменту в прошлом (by last Monday - к прошлому понедельнику, by yesterday - ко вчера). Вот и отлично. Значит можно, так сказать, пощупать результат.

// Present / Past / Future = описание времени
// Perfect Tense = совершенность
// Had done – Past Perfect – совершенность к моменту в прошлом (by yesterday – ко вчера)

// The bank had already risen the exchange rate by that time. – Банк уже поднял курс к тому времени.  
// I wanted to help him then. But he had already found a way out. – Я хотел тогда ему помочь. Но он уже нашел выход из ситуации.



// Had done
// Пощупать результат говорите? Ну например, вчера к вечеру я провела 6 уроков. Результат? Он самый. Где? В прошлом. Работаем с had done. I had given 6 lessons by yesterday evening. Ваша очередь! Несложно ведь. Форма Past Perfect очень простая для образования.
// Для всех лиц had и плюс третья форма нашего основного глагола-действия. Ну и будьте внимательны с ощущением времени. Перемещаться в нем мы пока не можем. Поэтому только оборачиваемся и вспоминаем, что результат-то был тогда, к моменту в прошлом (by that moment).

// Had done – результат в прошлом (had given by yesterday)
// Present Simple: every day/month/week, usually, often, sometimes, as a rule, all the time
// I, we, you, they, he, she, it + had done + by that moment
// I had given 6 lessons by yesterday evening. – Ко вчерашнему вечеру я провела 6 уроков.

// By the end of the year they had calculated all the business expenses. – К концу года они подсчитали все расходы на производство.
// By that moment Robert Taylor had already inherited his aunt's estate. – К тому моменту Роберт Тейлор уже унаследовал поместье своей тетушки.



// Had not done… Had you done
// I had done, you had done, he had done... Удобно, когда форма времени настолько проста в образовании. Выбрали действие и сразу получили универсальную форму для всех лиц. В предложениях – отрицаниях и предложениях – вопросах ситуация похожая.
// Итак, для отрицаний добавляем not к нашей форме и получаем had not done, а в вопросах ставим had на первое место и добавляем местоимение.

// Отрицаем: I, we, you, they, he, she, it + had not done + by that moment
// Had + I, we, you, they, he, she, it + done + by that moment? – Yes, I had.

// They had not exceeded the costs before. I have no idea why it happened last quarter. – Они до этого не превышали расходов. Не имею ни малейшего представления, почему это произошло в прошлом квартале.



// Had done и другие действия
// Итак, форма had done как Perfect Tense предполагает наличие определенного результата. И этот результат присутствует к определенному моменту в прошлом (by that moment, by last Monday). Иными словами, у нас есть два действия в прошлом, но одно (первое) закончилось до того, как началось второе.
// Первое — в этом случае это Past Perfect / had done форма, второе — Past Simple – did. В подобных ситуациях части предложения соединяются при помощи: when – когда, before – до того, как, after – после того, как и т.д. I bought a new house after I had inherited plenty of money. – Я купила новый дом после того, как унаследовала много денег.
// Здесь очень важно хорошо представлять очередность событий в прошлом. 

// Had done = результат + к какому моменту в прошлом
// Момент = время (by last Monday) / другое действие ( by the moment I came)
// By the moment I came = when I came = before I came

// We had had a dinner before we left tips. – Мы пообедали до того, как оставили чаевые.
// We had calculated our expenses before we bought a car. – Мы подсчитали наши расходы до того, как купили машину.
// I bought a new house after I had inherited plenty of money. – Я купила новый дом после того, как унаследовала много денег.




